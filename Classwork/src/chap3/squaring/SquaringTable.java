package chap3.squaring;

public class SquaringTable {
	public static void main(String[] args) {
		int base = 1;
		System.out.println("base\tbase^2");
		System.out.println(base + "\t" + base*base);
		base++;
		System.out.println(base + "\t" + base*base);
		base++;
		System.out.println(base + "\t" + base*base);
		base++;
		System.out.println(base + "\t" + base*base);
		base++;
		System.out.println(base + "\t" + base*base);
	}
}
